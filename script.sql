CREATE TABLE ESTADO_PRODUCTO(
    ID_ESTADO_PRODUCTO INT,
    NOMBRE_ESTADO_PRODUCTO VARCHAR2(150) UNIQUE NOT NULL,
    CONSTRAINT PK_ESTADO_PRODUCTO PRIMARY KEY (ID_ESTADO_PRODUCTO)
);

CREATE TABLE CATEGORIA(
    ID_CATEGORIA INT,
    NOMBRE_CATEGORIA VARCHAR2(200) UNIQUE NOT NULL,
    CONSTRAINT PK_CATEGORIA PRIMARY KEY (ID_CATEGORIA)
);

CREATE TABLE PRODUCTO(
    ID_PRODUCTO INT,
    ID_ESTADO_PRODUCTO INT,
    ID_CATEGORIA INT,
    NOMBRE_PRODUCTO VARCHAR2(200) UNIQUE NOT NULL,
    STOCK INT NOT NULL,
    PRECIO NUMBER(15,2) NOT NULL,
    IMAGEN BLOB NOT NULL,
    CONSTRAINT PK_PRODUCTO PRIMARY KEY (ID_PRODUCTO),
    CONSTRAINT FK_ESTADO_PRODUCTO FOREIGN KEY (ID_ESTADO_PRODUCTO)
    REFERENCES ESTADO_PRODUCTO (ID_ESTADO_PRODUCTO),
    CONSTRAINT FK_CATEGORIA FOREIGN KEY (ID_CATEGORIA)
    REFERENCES CATEGORIA (ID_CATEGORIA)
);

